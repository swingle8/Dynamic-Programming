class Solution 
{ 
    //Function to return max value that can be put in knapsack of capacity W.
    static int knapSack(int W, int wt[], int val[], int n) 
    { 
         int i = 0;
         int currVal = 0;
         //int currWeight = 0;
         return getValue (W, wt, val, i, currVal);
    } 
    
    static int getValue (int W, int wt[], int val[], int i, int currVal) {
        if (W < 0)
            return currVal-val[i-1];
        if (i == wt.length || W == 0)
            return currVal;
           
        return Math.max(getValue (W, wt, val, i+1, currVal), getValue (W-wt[i], wt, val, i+1, currVal+val[i]));
    }
}


